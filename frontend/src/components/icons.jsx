export const getStemIcon = (stemName) => {
  const name = stemName.toLowerCase();
  if (name.includes('vocal')) return <MicIcon />;
  if (name.includes('drum')) return <DrumIcon />;
  if (name.includes('bass') || name.includes('guitar')) return <GuitarIcon />;
  if (name.includes('piano') || name.includes('synth')) return <PianoIcon />;
  return <MusicNoteIcon />;
};
